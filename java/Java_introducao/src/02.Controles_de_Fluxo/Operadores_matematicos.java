public class Operadores_matematicos {
    public static void main(String[] args) {
        System.out.println(8+2);
        System.out.println(8-2);
        System.out.println(8*2);
        System.out.println(8/2);

        int a = 20;
        float b = 2.2f;

        System.out.println(a/b);

        b += 0.8F;
        System.out.println(b);

        b++;
        System.out.println(b);

    }
}
